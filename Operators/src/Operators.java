public class Operators {

    // additive operator
    static int add(int x, int y) {
        return x + y;
    }

    // subtraction operator
    static int sub(int x, int y) {
        return x - y;
    }

    // multiplication operator
    static int mult(int x, int y) {
        return x * y;
    }

    // division operator
    static int div(int x, int y) {
        return x / y;
    }

    // remainder operator
    static int mod(int x, int y) {
        return  x % y;
    }

    // less than
    static boolean lessThan(int x, int y) {
        return x < y;
    }

    // equals to
    static boolean equalsTo(int x, int y) {
        return x == y;
    }

    // greater than
    static boolean greatearThan(int x, int y) {
        return x > y;
    }
}
